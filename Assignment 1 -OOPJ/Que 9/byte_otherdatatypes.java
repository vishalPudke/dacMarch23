class byte_otherdatatypes {
    public static void main(String args[]){
        byte value=5;
        Byte b = new Byte( value );
        short sh = b;
        int num = b;
        long lg = b;
        float fl = b;
        double d = b;
        System.out.println(sh);
        System.out.println(num);
        System.out.println(lg);
        System.out.println(fl);
        System.out.println(d);

    }
    
}
